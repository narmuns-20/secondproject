package com.conditionalloops;

public class Example4 {

	public static void main(String[] args) {


		/**
		 * 
		 * initialization;
		 * 
		 * while(condition
		 * {
		 * 		//print statement
		 * 		//increment/decrement statement
		 * }
		 * 
		 */

		//initialization;
		int x = 0;

		//condition
		while(x <= 7)
		{
			System.out.println(x);
			
			x++;
			
		}
		
	}
}
