package com.conditionalloops;

public class Example5 {

	public static void main(String[] args) {

		/**
		 * 
		 * initialization;
		 * 
		 * do
		 * {
		 * 		//print statement
		 * 		//increment/decrement statement
		 * }
		 * while(condition);
		 * 
		 */

		//initialization;
		int i = 2;

		//condition
		do
		{
			System.out.println(i);
			i++;
		}
		while(i <= -8);
		
	}

}