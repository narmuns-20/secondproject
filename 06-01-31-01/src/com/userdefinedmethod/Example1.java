package com.userdefinedmethod;

//user defined method

public class Example1 {
			
			//user defined method definition
			public static void getMessage()//method head/signature
			{
				//method body/implementation
				System.out.println("this is user defined method");
			}
			
			
			 //main method is predefined method
			public static void main(String[] args)
			{
				//calling user defined method
				getMessage();
			}

		}

		