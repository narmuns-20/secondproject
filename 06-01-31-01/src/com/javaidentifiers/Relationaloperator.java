package com.javaidentifiers;

public class Relationaloperator {

	public static void main(String[] args) {

		int a =67;
		int b =45;
		
		System.out.println(">: "+(a > b));
		System.out.println("<: "+(a < b));	
		System.out.println(">=: "+(a >= b));
		System.out.println("<=: "+(a <= b));
		System.out.println("==: "+(a == b));
		System.out.println("!=: "+(a != b));
				
		
	}

}
