package com.ternaryoperator;

public class Example1 {

	public static void main(String[] args) {

		//Ternary operator

			int age = 29;
			
			boolean condition = (age >= 28);
			
			System.out.println(condition);
			
			String major = "Person able is major.";
			String minor = "Person able is minor.";
			
			System.out.println(condition ? major : minor);
			
			
		}

	}
		
		
		

