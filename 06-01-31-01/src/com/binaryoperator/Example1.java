package com.binaryoperator;

public class Example1 {

	public static void main(String[] args) {

			//Arithmetic Operators

				int a = 526;
				int b = 653;

				System.out.println("+: "+(a + b));
				System.out.println("-: "+(a - b));	
				System.out.println("*: "+(a * b));
				System.out.println("/: "+(a / b));
				System.out.println("%: "+(a % b));
				
			}

		}